c = 5
d = 10

# penjumlahan
hasil_penjumlahan = c + d
print("Hasil penjumlahan angka: {}".format(hasil_penjumlahan))

# perkalian
hasil_perkalian = c * d
print("Hasil perkalian angka: {}".format(hasil_perkalian))

# pembagian
hasil_pembagian = c / d
print("Hasil pembagian angka: {}".format(hasil_pembagian))

# pengurangan
hasil_pengurangan = c - d
print("Hasil pengurangan angka: {}".format(hasil_pengurangan))

print(c==d)
print(c<d)
print(c!=d)
print(c>d)
print(c<=d)
print(c>=d)
